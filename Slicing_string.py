#Slicing: the start index and the end index, separated by a colon,
# to return a part of the string.

#slicing: specific index to index
b = "Hello, World!"
print(b[2:5])

#slicing from the start
b = "Hello, World!"
print(b[:5])

#slicing to the end
b = "Hello, World!"
print(b[2:])

# negative slicing
b = "Hello, World!"
print(b[-5:-2])




